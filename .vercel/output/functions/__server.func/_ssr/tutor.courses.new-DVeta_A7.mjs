function NewCourseRedirect() {
  const {
    supabaseUser,
    loading
  } = useAuth();
  const navigate = useNavigate();
  const created = reactExports.useRef(false);
  reactExports.useEffect(() => {
    if (loading || created.current) return;
    if (!supabaseUser) {
      navigate({
        to: "/"
      });
      return;
    }
    created.current = true;
    createDraft(supabaseUser.id).then((id) => navigate({
      to: "/tutor/courses/$courseId/edit",
      params: {
        courseId: id
      }
    })).catch((e) => {
      toast.error(e.message ?? "Could not create course");
      navigate({
        to: "/tutor/courses"
      });
    });
  }, [supabaseUser, loading, navigate]);
  return /* @__PURE__ */ jsxRuntimeExports.jsxs("div", { className: "min-h-screen bg-[var(--pw-bg)] text-[var(--pw-ink)]", children: [
    /* @__PURE__ */ jsxRuntimeExports.jsx(PWHeader, {}),
    /* @__PURE__ */ jsxRuntimeExports.jsx("div", { className: "grid place-items-center py-32 text-[var(--pw-ink-2)]", children: /* @__PURE__ */ jsxRuntimeExports.jsx(LoaderCircle, { className: "size-6 animate-spin" }) })
  ] });
}
